SELECT C.Region, SUM(O.Sales) AS TotalSales
FROM Orders O
JOIN Customers C ON O.CustomerID = C.CustomerID
GROUP BY C.Region;
SELECT P.Category, SUM(O.Sales) AS TotalSales
FROM Orders O
JOIN Products P ON O.ProductID = P.ProductID
GROUP BY P.Category;
SELECT C.CustomerName, SUM(O.Sales) AS TotalSpent
FROM Orders O
JOIN Customers C ON O.CustomerID = C.CustomerID
GROUP BY C.CustomerName
ORDER BY TotalSpent DESC
LIMIT 10;
SELECT P.ProductName, SUM(O.Quantity) AS TotalQuantity
FROM Orders O
JOIN Products P ON O.ProductID = P.ProductID
GROUP BY P.ProductName
ORDER BY TotalQuantity DESC
LIMIT 10;
SELECT O.OrderID, C.CustomerName, P.ProductName, O.Profit
FROM Orders O
JOIN Customers C ON O.CustomerID = C.CustomerID
JOIN Products P ON O.ProductID = P.ProductID
